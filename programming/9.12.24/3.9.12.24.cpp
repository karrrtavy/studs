void printBin(int value){
    int count = sizeof(value) * 8;
    for(int i = count - 1; i >= 0; --i){
        int m = 1 << i;
        int res = m & value;
        if(res)
            cout<< 1;
        else
            cout<< 0;
        if(i != 0){
            if(i % 4 == 0) cout << ' ';
            if(i % 8 == 0) cout << ' ';
        }
    }
    cout<<endl;
}

